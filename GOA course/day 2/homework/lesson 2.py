# Creating a string
my_string = "Hello, World!"

# Creating an integer
my_integer = 42

# Creating the string 'gamarjoba'
hello_str = "gamarjoba"

# Creating the string 'Element' ten times in one line
element_str = "Element" * 10

# Creating the integer 1000 by concatenating two integers
num1 = 10
num2 = 00  # This is just zero, to illustrate concatenation we can use strings
num_combined = int(str(num1) + str(num2))

# Creating the string 'hello world' by concatenating two strings
hello_world = "hello" + " " + "world"

# Creating a boolean without directly writing True or False
boolean_value = (10 > 5)  # Since 10 is greater than 5, this evaluates to True

# Output the results
print("String:", my_string)
print("Integer:", my_integer)
print("Hello String:", hello_str)
print("Element Repeated:", element_str)
print("Concatenated Integer:", num_combined)
print("Concatenated String:", hello_world)
print("Boolean Value:", boolean_value)